package joey.present.data;




public class TypePojo {
	// 分类编号
	private String typeno_;
	// 分类名
	private String typename_;
	// 分类地址
	private String typeupdateurl_;
	// 自动更新状态
	private String autoupdate_;
	
	
	public String getTypeno_() {
		return typeno_;
	}
	public void setTypeno_(String typeno) {
		typeno_ = typeno;
	}
	public String getTypename_() {
		return typename_;
	}
	public void setTypename_(String typename) {
		typename_ = typename;
	}
	public String getTypeupdateurl_() {
		return typeupdateurl_;
	}
	public void setTypeupdateurl_(String typeupdateurl) {
		typeupdateurl_ = typeupdateurl;
	}
	public String getAutoupdate_() {
		return autoupdate_;
	}
	public void setAutoupdate_(String autoupdate) {
		autoupdate_ = autoupdate;
	}
}
