package joey.present.data;

public class CalData {
	

	private String calTime;
	private String calCountry;
	private String calItem;
	private String calImportance;
	private String calLastValue;
	private String calPrediction;
	private String calActual;
	public String getCalTime() {
		return calTime;
	}
	public void setCalTime(String calTime) {
		this.calTime = calTime;
	}
	public String getCalCountry() {
		return calCountry;
	}
	public void setCalCountry(String calCountry) {
		this.calCountry = calCountry;
	}
	public String getCalItem() {
		return calItem;
	}
	public void setCalItem(String calItem) {
		this.calItem = calItem;
	}
	public String getCalImportance() {
		return calImportance;
	}
	public void setCalImportance(String calImportance) {
		this.calImportance = calImportance;
	}
	public String getCalLastValue() {
		return calLastValue;
	}
	public void setCalLastValue(String calLastValue) {
		this.calLastValue = calLastValue;
	}
	public String getCalPrediction() {
		return calPrediction;
	}
	public void setCalPrediction(String calPrediction) {
		this.calPrediction = calPrediction;
	}
	public String getCalActual() {
		return calActual;
	}
	public void setCalActual(String calActual) {
		this.calActual = calActual;
	}


}
